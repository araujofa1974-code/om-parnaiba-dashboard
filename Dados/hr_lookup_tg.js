// ============================================================
// FUNÇÃO DE CONSULTA
// Uso: getHeatRate('TG12', 155.3)  →  retorna HR em kcal/kWh
// Interpolação linear entre os dois pontos inteiros adjacentes
// Retorna null se unidade inválida ou P fora do range [100, 168.8]
// ============================================================

function getHeatRate(unidade, potencia_mw) {
  const tabela = HR_LOOKUP[unidade];
  if (!tabela) return null;
  if (potencia_mw < 100 || potencia_mw > 168.8) return null;

  const p_low  = Math.floor(potencia_mw);
  const p_high = p_low === 168 ? 168.8 : p_low + 1;
  const hr_low  = tabela[p_low]  ?? tabela[168];
  const hr_high = tabela[p_high] ?? tabela[168.8];

  // Interpolação linear entre p_low e p_high
  const frac = (potencia_mw - p_low) / (p_high - p_low);
  return parseFloat((hr_low + frac * (hr_high - hr_low)).toFixed(1));
}

// ============================================================
// FUNÇÃO PARA CÁLCULO DE ENERGIA TÉRMICA (kcal)
// Uso: getEnergiaTermica('TG12', 155.3, 5)
//   potencia_mw: geração no intervalo
//   intervalo_min: duração do intervalo (padrão 5 min)
//   retorna: kcal consumidas no intervalo
// ============================================================

function getEnergiaTermica(unidade, potencia_mw, intervalo_min = 5) {
  const hr = getHeatRate(unidade, potencia_mw);
  if (hr === null) return null;
  // kcal = HR [kcal/kWh] × P [MW] × 1000 [kW/MW] × intervalo [h]
  const horas = intervalo_min / 60;
  return parseFloat((hr * potencia_mw * 1000 * horas).toFixed(0));
}